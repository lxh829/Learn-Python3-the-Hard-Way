print(3+2+1-5+4%2-1/4+6)                  #求余运算于乘法除法是同一个优先级，同时出现时应从左往右顺序计算
print("Is it true that 3 + 2<5 - 7?")
print("3+2= ",3+2)     #计算3+2的数值
print(3+2<5-7)      #判断3+2是否小于5-7，是的打印true，否的话打印Flase

print("What is 3+2?",3+2)  #以都好为分界线，一次打印出来，先打印前面的字符串，然后在打印后面3+2的值
print("What is 5-7?",5-7)  #同样的道理，依次打印出来，先打印前面的字符串，然后在打印后面的计算值

print("Oh,that's why it's False.")  #仅仅打印字符串

